export function getDayNumber(date = new Date()) {
  const start = new Date(PLAN_START);
  start.setHours(0, 0, 0, 0);
  const d = new Date(date);
  d.setHours(0, 0, 0, 0);
  return Math.floor((d - start) / (1000 * 60 * 60 * 24)) + 1;
}

export function getPassageForDate(date = new Date()) {
  const day = getDayNumber(date);
  if (day < 1 || day > 200) return null;
  return { day, passage: readingPlan[day - 1] };
}
